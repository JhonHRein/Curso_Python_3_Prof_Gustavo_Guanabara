#Escreva um programa que converta uma temperatura digitando em graus celsius e Convertendo para Fahrenheit.
print()
print("_"*20)
celsius = float(input("Conversor de temperatura: ºC para ºf, digite o valor: "))
fahrenheit = ((9*celsius)/5)+32
print(f"A temperatura de {celsius}ºC corresponde a {fahrenheit}ºF:")